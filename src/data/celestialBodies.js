export function validateBodies() {
  const required = [
    'id',
    'name',
    'englishName',
    'radius',
    'displayRadius',
    'distance',
    'displayDistance',
    'rotationSpeed',
    'revolutionSpeed',
    'axialTilt',
    'orbitInclination',
    'texture',
    'description',
    'facts',
  ];

  return CELESTIAL_BODIES.flatMap((body) => required
    .filter((key) => body[key] === undefined || body[key] === null)
    .map((key) => `${body.id}.${key}`));
}
